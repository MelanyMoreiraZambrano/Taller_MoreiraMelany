package arbolesBinarios;

public class Datos {
	//------------------------------------------------------
	//				Atributos de la clase
	//------------------------------------------------------
	Integer valor;
	//------------------------------------------------------
	// 					Constructor Vacio
	//------------------------------------------------------
	public Datos() {
		valor=null;
	}
	public Datos(Integer valor) {
		this.valor=valor;
	}
	//------------------------------------------------------
	// 					GETTERS Y SETTERS
	//------------------------------------------------------
	public Integer getValor() {
		return valor;
	}
	public void setValor(Integer valor) {
		this.valor = valor;
	}
	
}
